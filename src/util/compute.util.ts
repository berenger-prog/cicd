export default class ComputeUtil {
  static add(a: number, b: number): number {
    return a + b;
  }

  static minus(a: number, b: number): number {
    return a - b;
  }

  static multiply(a: number, b: number): number {
    return a * b; //torepj
  }

  static divide(a: number, b: number): number {
    return a / b;
  }
}
